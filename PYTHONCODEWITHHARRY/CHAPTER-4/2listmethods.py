a = ["Apple", "Orange", 5, 345.06, False, "Aakash", "Rohan"]
print(a)
 # APPEND-IT ADDS THE GIVEN ELEMENT AT THE END 
a.append("Append")
print(a)

# SORT-IT SORTS THE LIST IN INCREASING ORDER
l1 = [0,10,5,3,114,21,58]
l1.sort()
print(l1)

# REVERSE-IT REVERSE THE LIST END-TO-FRONT
l1.reverse()
print(l1)

#INSERT-IT INSERTS THE ELEMENT AT A SPECIFIC INDEX NUMBER(index,what to insert)
l1.insert(2,100)
print(l1)

# POP-IT DELETE ELEMENT FROM PARTICULAR INDEX
l1.pop(2)
print(l1)

# REMOVE-IT REMOVE THE DESIRED ELEMENT FROM THE LIST
l1.remove(114)
print(l1)
