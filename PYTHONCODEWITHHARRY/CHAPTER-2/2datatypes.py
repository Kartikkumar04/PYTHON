                             # Data Types 
# a variable name can only starts with a letter or a underscore
# a variable name cannot start with a number
# no spaces are allowed in variable names

a = 1 # 1 is a integer

b = "kartik" # "kartik" is a string

c = 3.14 # 3.14 is a float  

d = True # True is a boolean value

e = None # None is a special value indicating no value

print(a,b,c,d,e)